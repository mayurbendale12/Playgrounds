//Projection a value from Property wrapper
@propertyWrapper
struct SmallNumberWithProjectedValue {
    private var number: Int
    private(set) var projectedValue: Bool

    var wrappedValue: Int {
        get { return number }
        set {
            if newValue > 12 {
                number = 12
                projectedValue = true
            } else {
                number = newValue
                projectedValue = false
            }
        }
    }

    init() {
        self.number = 0
        self.projectedValue = false
    }
}

struct SampleStructure {
    @SmallNumberWithProjectedValue public var someNumber: Int
}
var someStructure = SampleStructure()

someStructure.someNumber = 4
print(someStructure.$someNumber)
// Prints "false"

someStructure.someNumber = 55
print(someStructure.$someNumber)

//Accessing projectedValue in method
enum SizeSample {
    case small, large
}

struct SizedRectangle {
    @SmallNumberWithProjectedValue var height: Int
    @SmallNumberWithProjectedValue var width: Int

    mutating func resize(to size: SizeSample) -> Bool {
        switch size {
        case .small:
            height = 10
            width = 20
        case .large:
            height = 100
            width = 100
        }
        return $height || $width
    }
}
